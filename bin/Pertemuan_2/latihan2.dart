void main() {
  // 1. Menyimpan panjang dan lebar dalam variabel bertipe double
  // Menggunakan nilai dari contoh Input/Output
  double panjang = 10;
  double lebar = 5;

  // 2. Menggunakan rumus matematika untuk menghitung luas dan keliling
  // Rumus Luas = Panjang * Lebar
  double luas = panjang * lebar;

  // Rumus Keliling = 2 * (Panjang + Lebar)
  double keliling = 2 * (panjang + lebar);

  // 3. Menampilkan hasil perhitungan ke layar
  // Menampilkan input yang digunakan
  print("Panjang = $panjang");
  print("Lebar = $lebar");

  // Menampilkan hasil kalkulasi
  print("Luas = $luas");
  print("Keliling = $keliling");
}
