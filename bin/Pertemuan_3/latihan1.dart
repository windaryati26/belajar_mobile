void main() {
  int a = 10;
  int b = 4;

  print('=== Kalkulator Sederhana ===');
  print('a = $a, b = $b\n');

  // Perkalian
  print('Perkalian: $a * $b = ${a * b}');

  // Pembagian (hasil double)
  print('Pembagian: $a / $b = ${a / b}');

  // Pembagian bulat
  print('Pembagian bulat: $a ~/ $b = ${a ~/ b}');

  // Modulus
  print('Modulus: $a % $b = ${a % b}');
}
